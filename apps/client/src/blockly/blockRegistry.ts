function field(
  name: string,
  kind: FieldKind,
  defaultValue: string | number | boolean | null,
  options: Partial<Omit<BlockFieldSchema, "name" | "kind" | "defaultValue">> = {},
): BlockFieldSchema {
  return { name, kind, defaultValue, ...options };
}

function entry(entry: BlockRegistryEntry): BlockRegistryEntry {
  return entry;
}

